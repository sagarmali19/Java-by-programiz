public class Operators{
    public static void main(String[] args){
        // Arithmatic operator
        // declare variables
        int a = 12, b = 5;

        // addition operator
        System.out.println("a + b = " + (a + b));

        // subtraction operator
        System.out.println("a - b = " + (a - b));

        // multiplication operator
        System.out.println("a * b = " + (a * b));

        // division operator
        System.out.println("a / b = " + (a / b));

        // modulo operator
        System.out.println("a % b = " + (a % b));

        // Assignment operator
        // create variables
        int a1 = 4;
        int var;

        // assign value using =
        var = a1;
        System.out.println("var using =: " + var);

        // assign value using =+
        var += a1;
        System.out.println("var using +=: " + var);

        // assign value using =*
        var *= a1;
        System.out.println("var using *=: " + var);

        


    }
}